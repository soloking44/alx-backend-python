#!/usr/bin/env python3
"""It ontains a process that concatenates two string values."""


def concat(str1: str, str2: str) -> str:
    """
    it concatenates two string values.

    Args:
    - str1 (str): first string value to concatenate
    - str2 (str): second string value to concatenate

    Returns:
    - str: concatenated string value
    """
    return str1 + str2
