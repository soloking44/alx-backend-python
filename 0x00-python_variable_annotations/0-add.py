#!/usr/bin/env python3
"""
This contains a function that adds two numbers
"""


def add(a: float, b: float) -> float:
    """
    Adds two values together and returns the output.

    Parameters:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b
